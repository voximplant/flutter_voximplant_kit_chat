/// Authorization error reported by the Voximplant Kit Chat SDK.
enum KitChatAuthorizationError {
  /// Invalid mobile channel UUID.
  invalidChannelUuid,

  /// Invalid mobile channel token.
  invalidToken,

  /// Invalid client ID.
  invalidClientId,

  /// Unknown authorization error.
  unknown,
}
